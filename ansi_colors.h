#ifndef ANSI_COLORS_H
#define ANS_COLORS_H

// regular colorized text
#define BLA "\e[0;30m"
#define RED "\e[0;31m"
#define PUR "\e[0;35m"
#define CYN "\e[0;36m"
#define WHI "\e[0;37m"

// bold colorized text
#define BBLA "\e[1;30m"
#define BRED "\e[1;31m"
#define BPUR "\e[1;35m"
#define BCYN "\e[1;36m"
#define BWHI "\e[1;37m"

// background color
#define BLAB "\e[40m"
#define REDB "\e[41m"
#define PURB "\e[45m"
#define CYNB "\e[46m"
#define WHIB "\e[47m"

// bold high intensity colorized text
#define BIBLA "\e[1;90m"
#define BIRED "\e[1;91m"
#define BIPUR "\e[1;95m"
#define BICYN "\e[1;96m"
#define BIWHI "\e[1;97m"

// RESET
#define CRST "\e[0m"

#endif